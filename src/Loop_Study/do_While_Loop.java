package Loop_Study;

public class do_While_Loop {

	public static void main(String[] args) 
	{
		
		//I Want To Print 6 Table Increment Order
		
		int a=6;
		do 
		{
			System.out.println(a);
			a=a+6;
		} 
		while (a<=60);

		
         System.out.println("===============================");
		//I Want To Print 6 Table decrement Order

		int b=60;
		do 
		{
			System.out.println(b);
			b=b-6;
		} 
		while (b>=6);
	   
}	  
	
}

