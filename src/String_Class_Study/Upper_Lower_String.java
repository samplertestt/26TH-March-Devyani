package String_Class_Study;

public class Upper_Lower_String {

	public static void main(String[] args) {
		String Name="devyani";
		System.out.println(Name);
		System.out.println("============================");
		
		//By Using Upper Case Method
		String Name1="devyani";
		System.out.println(Name1.toUpperCase());
		String Name2=Name1.toUpperCase();	
		System.out.println("The Upper Case Value Is "+Name2);
		System.out.println("============================");
		
	    //By Using Lower Case Method
		String Name3="VELOCITY";
		System.out.println(Name3.toLowerCase());
		String Name4=Name3.toLowerCase();
		System.out.println("The Upper Case Value Is "+Name4);
	
		
	}

}
