package Casting;

public class Implicit_Casting {

	public static void main(String[] args) {
//		Converting lower data type info into higher data type info is called implicit casting. 
//		implicit casting is also called widening casting, 
//		where memory size goes on increasing, there is no data loss.

	int a=10;
	System.out.println(a);
	
	double b=a;
	System.out.println(b);
	
	}

}
